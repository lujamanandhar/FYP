from django.core.management.base import BaseCommand
from nutrition.models import FoodItem


class Command(BaseCommand):
    help = 'Seeds the database with 1000+ common food items based on USDA data'

    def handle(self, *args, **kwargs):
        self.stdout.write('Seeding large food database...')
        
        # Check existing count
        existing_count = FoodItem.objects.filter(is_custom=False).count()
        self.stdout.write(f'Found {existing_count} existing system foods')
        
        foods = self.generate_comprehensive_food_database()
        
        created_count = 0
        skipped_count = 0
        
        for food_data in foods:
            if not FoodItem.objects.filter(name=food_data['name'], is_custom=False).exists():
                FoodItem.objects.create(**food_data)
                created_count += 1
            else:
                skipped_count += 1
        
        total = FoodItem.objects.filter(is_custom=False).count()
        self.stdout.write(self.style.SUCCESS(f'Successfully seeded {created_count} new food items'))
        self.stdout.write(self.style.SUCCESS(f'Total system foods in database: {total}'))
        if skipped_count > 0:
            self.stdout.write(self.style.WARNING(f'Skipped {skipped_count} existing items'))
    
